class SystemMemory():
    musics = []
    podcasts = []
    soundEffects = []

    def addMusic(self, newMusic):
        self.musics.append(newMusic)

    def addPodcast(self, newPodcast):
        self.podcasts.append(newPodcast)

    def addSoundEffect(self, newEffect):
        self.soundEffects.append(newEffect)

    